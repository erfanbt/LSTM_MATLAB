for k=1:10
    a(k)=(tanh(-1+0.2*k)-tanh(-1+0.2*(k-1)))/0.2;
    b(k)=tanh(-1+0.2*k)-a(k)*(-1+0.2*k);
end

x = [-1:0.001:1];
x1 = [-1:0.001:-0.8];
x2 = [-0.8:0.001:-0.6];
x3 = [-0.6:0.001:-0.4];
x4 = [-0.4:0.001:-0.2];
x5 = [-0.2:0.001:0];
x6 = [0:0.001:0.2];
x7 = [0.2:0.001:0.4];
x8 = [0.4:0.001:0.6];
x9 = [0.6:0.001:0.8];
x10 = [0.8:0.001:1];

y=tanh(x);
y1 = a(1)*x1+b(1);
y2 = a(2)*x2+b(2);
y3 = a(3)*x3+b(3);
y4 = a(4)*x4+b(4);
y5 = a(5)*x5+b(5);
y6 = a(6)*x6+b(6);
y7 = a(7)*x7+b(7);
y8 = a(8)*x8+b(8);
y9 = a(9)*x9+b(9);
y10 = a(10)*x10+b(10);

plot(x, y, x1, y1, x2, y2, x3, y3, x4, y4, x5, y5, x6, y6, x7, y7, x8, y8, x9, y9, x10, y10)